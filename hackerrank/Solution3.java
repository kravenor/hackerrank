package hackerrank;

import java.util.Scanner;

/**
 *
 * @author Simone
 */
public class Solution3 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("================================");
        for (int i = 0; i < 3; i++) {
            String s1 = sc.next();
            int x = sc.nextInt();
            System.out.printf(s1 + String.format("%03d", x));
        }
        System.out.println("================================");

    }

}
